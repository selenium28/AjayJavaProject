package Practices;

public class ArrayString {

	public static void main(String[] args) {
	String str[] = new String [7];
	str[0] = ("Ajay");
	str[1] = ("gundekar");
	str[2] = ("raj");
	str[3] = ("vijay");
	str[4] = ("duggu");
	str[5] = ("tarush");
	str[6] = ("Ganesh");
	 
	for(int i=6; i>=0; i--) {
		System.out.println(str[i]);
	}
	}

}
